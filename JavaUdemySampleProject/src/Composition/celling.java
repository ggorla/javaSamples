package Composition;

public class celling {
    private int height;
    private int paintedColor;

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }

    public celling(int height, int paintedColor) {
        this.height = height;
        this.paintedColor = paintedColor;
    }
}
